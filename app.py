# Load Pipeline
@st.cache_resource
def load_pipeline():
    base_dir = os.path.dirname(__file__)
    pipeline_path = os.path.join(base_dir, 'loan_model_pipeline.joblib')
    return joblib.load(pipeline_path)

try:
    pipeline = load_pipeline()
    model = pipeline['model']
    imputer_num = pipeline['imputer_num']
    imputer_cat = pipeline['imputer_cat']
    le_edu = pipeline['le_edu']
    ohe = pipeline['ohe']
    scaler = pipeline['scaler']
    feature_names = pipeline['feature_names']
    num_cols = pipeline['num_cols']
    cat_cols = pipeline['cat_cols']
    ohe_cols = pipeline['ohe_cols']
    pipeline_loaded = True
except Exception as e:
    st.error("Error loading model pipeline. Please make sure 'loan_model_pipeline.joblib' is generated by running 'train_model.py'.")
    pipeline_loaded = False

# Sidebar Info
