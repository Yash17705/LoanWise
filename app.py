    st.image("https://img.icons8.com/isometric/100/loan.png", width=80)
    st.title("Loan System")
    st.markdown("This intelligent system evaluates customer creditworthiness using a trained **Logistic Regression** model.")
    st.info("Fill in the applicant and financial details to run a real-time prediction.")
